  int a = 5;
  switch (a) {
    case 1:
      {
        print("one");
      }
      break;
    case 2:
      {
        print("two");
        break;
      }
    case 3:
      {
        print("three");
        break;
      }
    case 4:
      {
        print("four");
        break;
      }
    case 5:
      {
        print("five");
        break;
      }
    default:
      {
        print("no match");
        break;
      }
  }
}
